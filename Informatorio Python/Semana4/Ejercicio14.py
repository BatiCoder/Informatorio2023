
# 14-Crea una función llamada encontrar_maximo que tome una lista de números
# como parámetro y devuelva el número máximo de la lista.

def encontrar_maximo(lista):

    # Retorna el valor máximo de la lista
    return max(lista)

# Llama a la función encontrar_maximo con una lista y imprime el valor máximo
print(encontrar_maximo([1, 2, 3, 12, 23, 32, 45, 70]))

# La salida en este caso sería 70, ya que 70 es el valor máximo en la lista 
# [1, 2, 3, 12, 23, 32, 45, 70].