
# 7-Escribe un programa que calcule el área de un triángulo a partir de la base y la
# altura dadas.

base=float(input("Ingrese la medida de la base del triangulo: "))
altura=float(input("Ingrese la medida de la altura del triangulo: "))

area_triangulo=(base*altura)/2

print(f"El area del triangulo es {area_triangulo}")


