
# 9-Escribe un programa que solicite al usuario dos números y luego imprima la
# suma, la resta, la multiplicación y la división de los dos números.

num1=float(input("Ingrese el 1er numero: "))
num2=float(input("Ingrese el 2do numero: "))

suma=num1+num2
resta=num1-num2
multiplicacion=num1*num2
division=num1/num2

print("\nResultado de la suma entre los dos numeros:",suma)
print("Resultado de la resta entre los dos numeros:",resta)
print("Resultado de la multiplicacion entre los dos numeros:",multiplicacion)
print("Resultado de la division entre los dos numeros:",division)
