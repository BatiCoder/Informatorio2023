# 10-Crear una lista con los nombres de tres frutas y eliminar la segunda fruta de la
# lista. Mostrar la lista resultante.

lista_frutas = ['Banana', 'Manzana', 'Pera']

# Eliminar la segunda fruta de la lista
del lista_frutas[1]  # O también se puede usar: listas_frutas.pop(1)

# Mostrar la lista resultante
print(lista_frutas)

