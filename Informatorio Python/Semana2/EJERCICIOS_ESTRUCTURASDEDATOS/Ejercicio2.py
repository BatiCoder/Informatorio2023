# 2-Crear una lista con los nombres de tres ciudades y agregar una cuarta ciudad al
# final de la lista.

# Crear una lista de ciudades
ciudades = ["Madrid", "Barcelona", "Valencia"]

# Agregar una cuarta ciudad al final de la lista
ciudades.append("Sevilla")

# Mostrar la lista de ciudades completa
print(ciudades)

# Se ha creado una lista llamada ciudades que contiene tres elementos: 'Madrid', 'Barcelona' y 'Valencia'.
# Para agregar una cuarta ciudad al final de la lista, se usa el método append() de la lista. Este método
# agrega el elemento proporcionado como argumento al final de la lista.
# Finalmente, para mostrar la lista completa de ciudades, se llama a la función print() y se pasa la
# variable ciudades como argumento.
