# 6-Crear un set/conjunto con los números impares del 1 al 10 y mostrar el número
# de elementos en el conjunto.

# Crear un conjunto.
numeros_impares = {1, 3, 5, 7, 9}

# Mostrar por pantalla la cantidad de elementos del conjunto
print(len(numeros_impares))

# En este ejemplo, se ha creado un conjunto llamado numeros_impares que contiene los números
# impares del 1 al 10. Para crear este conjunto, se han incluido manualmente los números
# impares como elementos del conjunto.Para mostrar el número de elementos en el conjunto, se
# utiliza la función len(), que devuelve el número de elementos en el conjunto. Finalmente,
# para mostrar el número de elementos en la consola, se llama a la función print() y se
# pasa len(numeros_impares) como argumento.
