# 12-Crear una lista con los nombres de tres países y reemplazar el segundo país de
# la lista por otro país. Mostrar la lista resultante.

# Crear una lista con los nombres de tres países
paises = ["Inglaterra", "Estados Unidos", "Australia"]

# Reemplazar el segundo país de la lista por otro país
paises[1] = "Argentina"

# Mostrar la lista resultante
print(paises)

# Este código reemplazará el segundo país ("Estados Unidos") de la lista paises por "Argentina"
# y mostrará la lista resultante, que en este caso sería ['Inglaterra', 'Argentina', 'Australia']
