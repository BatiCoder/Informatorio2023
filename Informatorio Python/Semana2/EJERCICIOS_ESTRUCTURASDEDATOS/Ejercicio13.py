# 13-Crear una lista con los nombres de tres colores y agregar dos colores más al
# final de la lista. Mostrar la lista resultante.

# Crear una lista con los nombres de tres colores
colores = ["Azul", "Rojo", "Verde"]

# Agregar dos colores al final de la lista utilizando el método append()
colores.append("Morado")
colores.append("Amarillo")

# Mostrar la lista resultante
print(colores)

