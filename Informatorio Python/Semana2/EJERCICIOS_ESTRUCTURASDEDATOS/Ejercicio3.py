# 3-Crear una lista con los nombres de tres países y mostrar el segundo país de la
# lista.

# Crear
paises = ["Argentina", "Brasil", "Chile"]

# Mostrar por pantalla el 2do país de la lista anterior.
print(paises[1])

# En este ejemplo, se ha creado una lista llamada paises que contiene tres elementos: 'España', 'Francia' y 'Italia'.
# Para mostrar el segundo país de la lista, se accede al elemento en el índice 1 de la lista usando la sintaxis de
# corchetes []. Los índices de lista en Python comienzan en 0, por lo que el segundo elemento de la lista tiene un
# índice de 1.
# Finalmente, para mostrar el segundo país de la lista, se llama a la función print() y se pasa paises[1] como argumento.
