# 1-Crear un diccionario con los nombres de tres frutas y sus respectivos precios y
# mostrar el diccionario completo.

# Crear un diccionario con frutas y sus precios
frutas = {"manzana": 2.0, "naranja": 1.5, "plátano": 0.5}

# Mostrar el diccionario completo
print(frutas)

# En este ejemplo, se ha creado un diccionario llamado frutas que contiene tres elementos:
# 'manzana', 'naranja' y 'plátano'. Los precios de cada fruta se han asignado como valores
# de diccionario correspondientes a las claves de las frutas.Para mostrar el diccionario
# completo, simplemente llamamos a la función print() y le pasamos la variable frutas como argumento.
# Esto mostrará el diccionario completo en la consola. El resultado en la consola sería:
# {'manzana': 2.0, 'naranja': 1.5, 'plátano': 0.5}
